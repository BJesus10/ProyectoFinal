    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // Paso 1: Leer datos del formulario
        String dni = txtDni.getText();
        String nombres = txtNombres.getText();
         String telefono = txtTelefono.getText();
           String email = txtEmail.getText();
String asunto = txtAsunto.getText();
String docRef = txtDocRef.getText();
String prioridad = cmbPrioridad.getSelectedItem().toString();

// Validar que no estén vacíos
if (dni.isEmpty() || nombres.isEmpty() || telefono.isEmpty() || email.isEmpty() || asunto.isEmpty() || docRef.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Por favor completa todos los campos.");
    return;
}

// Paso 2: Crear UsuarioInteresado
UsuarioInteresado usuario = new UsuarioInteresado(dni, nombres, telefono, email, true); // true = es interno ULima

// Paso 3: Generar ID único (por tamaño de lista)
GestorTramites gestor = GestorTramitesSingleton.getInstancia();
int nuevoId = gestor.obtenerExpedientes().size() + 1;

// Paso 4: Crear expediente
Expediente expediente = new Expediente(nuevoId, prioridad, usuario, asunto, docRef);
expediente.moverADependencia("Mesa de Partes");

// Paso 5: Guardar en la lista
gestor.agregarExpediente(expediente);

// Confirmación
JOptionPane.showMessageDialog(this, "Expediente registrado con ID: " + nuevoId);

// Borrar  
txtDni.setText("");
txtNombres.setText("");
txtTelefono.setText("");
txtEmail.setText("");
txtAsunto.setText("");
txtDocRef.setText("");
        
        
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

